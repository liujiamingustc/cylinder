   +4.341229e+003  * y**9  -  2.218901e+004  * y**8                 &
   +4.851211e+004  * y**7  -  5.923957e+004  * y**6                 &
   +4.425061e+004  * y**5  -  2.080003e+004  * y**4                 &
   +6.078162e+003  * y**3  -  1.039484e+003  * y**2                 &
   +8.685630e+001  * y**1  -  6.519220e-002

  END FUNCTION
